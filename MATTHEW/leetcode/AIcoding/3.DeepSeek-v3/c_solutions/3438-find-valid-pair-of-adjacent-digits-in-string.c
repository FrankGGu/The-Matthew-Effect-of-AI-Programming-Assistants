int findValidPair(char* s) {
    for (int i = 0; s[i + 1] != '\0'; i++) {
        if ((s[i] == '0' && s[i + 1] == '1') || 
            (s[i] == '1' && s[i + 1] == '0') ||
            (s[i] == '2' && s[i + 1] == '3') ||
            (s[i] == '3' && s[i + 1] == '2') ||
            (s[i] == '4' && s[i + 1] == '5') ||
            (s[i] == '5' && s[i + 1] == '4') ||
            (s[i] == '6' && s[i + 1] == '7') ||
            (s[i] == '7' && s[i + 1] == '6') ||
            (s[i] == '8' && s[i + 1] == '9') ||
            (s[i] == '9' && s[i + 1] == '8')) {
            return i;
        }
    }
    return -1;
}