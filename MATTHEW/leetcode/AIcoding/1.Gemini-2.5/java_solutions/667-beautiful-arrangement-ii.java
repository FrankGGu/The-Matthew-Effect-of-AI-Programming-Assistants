class Solution {
    public int[] constructArray(int n, int k) {
        int[] ans = new int[n];

        int left = 1;
        int right = k + 1;

        // Fill the first k+1 elements to generate k distinct differences
        // The pattern alternates between smallest available and largest available numbers
        // from the range [1, k+1]. This generates differences k, k-1, ..., 1.
        for (int i = 0; i <= k; i++) {
            if (i % 2 == 0) {
                ans[i] = left++;
            } else {
                ans[i] = right--;
            }
        }

        // Fill the remaining n - (k+1) elements in increasing order.
        // These numbers are k+2, k+3, ..., n.
        // The differences generated by these elements will all be 1,
        // which is already part of the k distinct differences generated earlier.
        for (int i = k + 1; i < n; i++) {
            ans[i] = i + 1;
        }

        return ans;
    }
}