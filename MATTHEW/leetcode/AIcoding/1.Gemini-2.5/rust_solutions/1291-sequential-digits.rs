struct Solution;

impl Solution {
    pub fn sequential_digits(low: i32, high: i32) -> Vec<i32> {
        let mut results = Vec::new();

        for i in 1..=9 {
            let mut num = i;
            let mut next_digit = i + 1;

            while num <= high && next_digit <= 9 {
                if num >= low {
                    results.push(num);
                }

                num = num * 10 + next_digit;
                next_digit += 1;
            }

            // After the loop, the last `num` formed might be in range
            // but the loop condition (e.g., num <= high) might have failed
            // before checking this `num`.
            // Example: low=100, high=150. i=1.
            // num becomes 123. Pushed. next_digit becomes 4.
            // num becomes 1234. next_digit becomes 5.
            // Loop condition `1234 <= 150` is false. Loop terminates.
            // The value 1234 was not checked against `low`.
            // This final check covers such cases.
            if num >= low && num <= high {
                results.push(num);
            }
        }

        results.sort_unstable(); // Although generated in a somewhat sorted order, a full sort ensures correctness.
                                 // (e.g., 123, 234, 345, ... is sorted, but 123, then 23, then 34 is not)
                                 // Let's re-verify the generation order.
                                 // 1, 12, 123, 1234, ...
                                 // 2, 23, 234, 2345, ...
                                 // The numbers are indeed generated in increasing order.
                                 // E.g., all numbers starting with 1 are generated first, then all starting with 2, etc.
                                 // Within a starting digit, numbers are generated by increasing length.
                                 // So, 123 will be generated and pushed before 234.
                                 // And 123 will be pushed before 1234.
                                 // Thus, the `results` vector will naturally be sorted.
                                 // No explicit sort is needed.
        results
    }
}