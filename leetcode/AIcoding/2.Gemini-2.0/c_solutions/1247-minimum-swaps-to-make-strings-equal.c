#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int minimumSwaps(char * s1, char * s2){
    int xy = 0, yx = 0;
    for (int i = 0; s1[i] != '\0'; i++) {
        if (s1[i] == 'x' && s2[i] == 'y') {
            xy++;
        } else if (s1[i] == 'y' && s2[i] == 'x') {
            yx++;
        }
    }
    if ((xy + yx) % 2 != 0) {
        return -1;
    }
    return xy / 2 + yx / 2 + xy % 2 + yx % 2;
}