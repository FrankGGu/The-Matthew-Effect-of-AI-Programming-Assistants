class Solution:
    def divisorSubstrings(self, num: int, k: int) -> int:
        s = str(num)
        count = 0
        for i in range(len(s) - k + 1):
            substring = s[i:i+k]
            if int(substring) != 0 and num % int(substring) == 0:
                count += 1
        return count