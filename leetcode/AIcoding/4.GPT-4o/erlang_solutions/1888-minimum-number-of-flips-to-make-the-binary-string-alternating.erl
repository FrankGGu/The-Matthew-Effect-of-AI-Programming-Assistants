-module(solution).
-export([min_flips/1]).

min_flips(Binary) ->
    Length = length(Binary),
    Flip1 = count_flips(Binary, "010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101"),
    Flip2 = count_flips(Binary, "101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010101010"),
    min(Flip1, Flip2).

count_flips([], []) -> 0;
count_flips([H1 | T1], [H2 | T2]) -> 
    (if H1 =:= H2 -> 0; true -> 1 end) + count_flips(T1, T2).