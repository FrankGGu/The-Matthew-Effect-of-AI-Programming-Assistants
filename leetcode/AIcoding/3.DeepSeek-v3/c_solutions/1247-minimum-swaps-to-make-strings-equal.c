int minimumSwap(char* s1, char* s2) {
    int xy = 0, yx = 0;
    int len = strlen(s1);

    for (int i = 0; i < len; i++) {
        if (s1[i] == 'x' && s2[i] == 'y') {
            xy++;
        } else if (s1[i] == 'y' && s2[i] == 'x') {
            yx++;
        }
    }

    if ((xy + yx) % 2 == 1) {
        return -1;
    }

    return xy / 2 + yx / 2 + (xy % 2) * 2;
}